using UnityEngine;
using System.Collections;


public class LostFoodCount : MonoBehaviour 
{
	/*************************************
 * // TODO: UNUSED CURRENTLY OBSOLETE
	EsophagusDebugConfig config;
	private int lostFoodCount;
	private int maxLostFoodAllowed = 10;

	// Use this for initialization
	void Start () 
	{
		lostFoodCount = 0;	
		GameObject debugger = GameObject.Find("Debugger");
		config = debugger.GetComponent<EsophagusDebugConfig>();
	}
	
	// Update is called once per frame
	void Update () 
	{
		if(config.debugActive)
		{
			maxLostFoodAllowed = config.maxLostFoodAmount;
		}
	}

	void OnGUI()
	{
		GUI.Label(new Rect(15, 15, 100, 50), "Lost food: " + lostFoodCount);
	}

	public int getLostFoodCount()
	{
		return lostFoodCount;
	}

	public void increaseLostFoodCount()
	{
		lostFoodCount++;
	}
	********************************************************/
}
